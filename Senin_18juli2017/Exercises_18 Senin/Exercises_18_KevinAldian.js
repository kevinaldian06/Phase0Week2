//Mengenal penggunaan Iteration (For) dalam JavaScript

for(var deret = 0; deret <= 20; deret += 1) {
  console.log(deret + ' - I LOVE CODING');
}

console.log('-------------------------------');

for(var deret = 20; deret > 0; deret -= 1) {
  console.log(deret + ' - I will become fullstack developer');
}